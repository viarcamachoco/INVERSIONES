const fs = require("node:fs");
const path = require("node:path");
const jwt = require("jsonwebtoken");

function readJwtSecret(envPath) {
  const lines = fs.readFileSync(envPath, "utf8").split(/\r?\n/);
  const jwtLine = lines.find((line) => line.startsWith("JWT_SECRET="));

  if (!jwtLine) {
    throw new Error("No se encontro JWT_SECRET en .env del backend");
  }

  const secret = jwtLine.slice("JWT_SECRET=".length).trim();
  if (!secret) {
    throw new Error("JWT_SECRET esta vacio en .env del backend");
  }

  if (secret.length <= 32 || secret.includes("REPLACE_WITH")) {
    throw new Error("JWT_SECRET invalido para desarrollo. Usa un valor real de mas de 32 caracteres.");
  }

  return secret;
}

function main() {
  const backendRoot = process.cwd();
  const envPath = path.resolve(backendRoot, ".env");

  if (!fs.existsSync(envPath)) {
    throw new Error("No existe .env del backend. Crea projects/rest-api/inversions_api/.env");
  }

  const secret = readJwtSecret(envPath);
  const token = jwt.sign(
    {
      sub: process.env.DEV_TOKEN_SUB ?? "dev-user",
      role: process.env.DEV_TOKEN_ROLE ?? "trader",
      email: process.env.DEV_TOKEN_EMAIL ?? "dev@local"
    },
    secret,
    {
      algorithm: "HS256",
      expiresIn: process.env.DEV_TOKEN_EXPIRES_IN ?? "7d"
    }
  );

  const frontendEnvPath = path.resolve(
    backendRoot,
    "..",
    "..",
    "pwa",
    "inversions_app",
    ".env.local"
  );

  const content = [
    "# Auto-generated by rest-api script sync-dev-token-to-frontend.js",
    `VITE_DEV_BEARER_TOKEN=${token}`,
    ""
  ].join("\n");

  fs.writeFileSync(frontendEnvPath, content, "utf8");
  console.log(`Token sincronizado en ${frontendEnvPath}`);
}

main();